// Utility function to get today's quote
export function getTodaysQuote(): (typeof motivationalQuotes)[0] {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) /
      (1000 * 60 * 60 * 24),
  );
  return motivationalQuotes[dayOfYear % motivationalQuotes.length];
}

// Utility function to get today's cat
export function getTodaysCat(): (typeof dailyCats)[0] {
  const today = new Date();
  const dayOfWeek = today.getDay();
  return dailyCats[dayOfWeek % dailyCats.length];
}

// Utility function to check if it's user's birthday (for demo, let's use a specific date)
export function isBirthday(): boolean {
  const today = new Date();
  // For demo purposes, let's make it someone's birthday on the 15th of each month
  return today.getDate() === 15;
}

// Utility function to get random birthday message
export function getBirthdayMessage(): (typeof birthdayMessages)[0] {
  const randomIndex = Math.floor(Math.random() * birthdayMessages.length);
  return birthdayMessages[randomIndex];
}
